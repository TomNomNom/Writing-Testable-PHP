# Types of tests

## Unit tests
* Should:
  * Run quickly
  * Be stable
  * Make up the majority of your tests
* Shouldn't:
  * Use anything you're not testing

## Integration tests
* Should:
  * Run quite quickly
  * Be quite stable
  * Make up less of your tests

## Full-stack tests
* Should:
  * Be used sparingly
* Will:
  * Be brittle
  * Take a long time to run

